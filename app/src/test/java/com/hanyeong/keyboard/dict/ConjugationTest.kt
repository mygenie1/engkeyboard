package com.hanyeong.keyboard.dict

import org.junit.Assert.assertEquals
import org.junit.Assert.assertNull
import org.junit.Test

/**
 * 용언 활용형 → 원형(사전형) 복원 자동 검사.
 *
 * 두 방향을 봅니다.
 *  1) 복원 성공: 대표 활용형이 정확한 원형으로 복원돼야 한다.
 *  2) 오인식 방지: 흔한 문장 표본의 단어들이 '엉뚱한 원형'으로 복원되지
 *     않아야 한다(= null 이어야 한다). "틀린 추천이 빈 추천보다 나쁘다."
 *
 * 사전은 아래 [BASES]로 흉내 냅니다(실제 앱 사전의 부분집합).
 * resolveBase 는 이 집합에 있는 원형만 후보로 인정합니다.
 */
class ConjugationTest {

    /** 테스트용 가짜 사전(원형 집합). 활용형 복원 후보는 여기 있어야 인정됨. */
    private val BASES = setOf(
        // 규칙 용언
        "먹다", "가다", "오다", "보다", "주다", "쓰다", "크다", "자다", "사다",
        "차다", "타다", "갈다", "살다", "알다", "나다", "울다",
        "마시다", "기다리다", "되다",
        "예쁘다", "나쁘다", "아프다", "바쁘다", "슬프다", "고프다",
        "하다", "공부하다", "사랑하다", "좋아하다",
        // 불규칙 용언 (앱 등록표에도 있는 초고빈도)
        "춥다", "덥다", "돕다", "굽다", "맵다", "쉽다", "어렵다", "무겁다",
        "고맙다", "즐겁다", "반갑다",
        "낫다", "짓다", "붓다", "젓다",
        "모르다", "부르다", "고르다", "빠르다", "흐르다",
        "그렇다", "빨갛다", "파랗다",
        // 오인식(중의성) 확인용
        "듣다", "걷다", "묻다", "들다", "걸다", "물다"
    )

    private fun resolve(surface: String): String? =
        Conjugation.resolveBase(surface) { it in BASES }

    // ── 1) 복원 성공 케이스 ─────────────────────────────────────────
    @Test
    fun `규칙 활용 복원`() {
        val cases = mapOf(
            // 사용자 요청 어미: 었/았/어요/네/니/고/지만/어서 (+ 현재 반말)
            "먹어" to "먹다", "먹었어" to "먹다", "먹어요" to "먹다", "먹어서" to "먹다",
            "먹네" to "먹다", "먹니" to "먹다", "먹고" to "먹다", "먹지만" to "먹다",
            "먹었다" to "먹다", "먹었어요" to "먹다", "먹었지만" to "먹다", "먹었고" to "먹다",
            // 모음 어간 (모음 축약)
            "갔어" to "가다", "왔어" to "오다", "봤어" to "보다", "줬어" to "주다",
            "가요" to "가다", "잤어" to "자다",
            // ㅡ 탈락
            "썼어" to "쓰다", "컸어" to "크다",
            "예뻐" to "예쁘다", "아파" to "아프다", "바빠" to "바쁘다", "슬퍼" to "슬프다",
            // ㅣ/ㅚ
            "마셔" to "마시다", "마셨어" to "마시다",
            "기다려" to "기다리다", "기다렸어" to "기다리다",
            "됐어" to "되다",
            // 하다류(여 불규칙)
            "했어" to "하다", "공부했어" to "공부하다", "공부해" to "공부하다",
            "사랑해" to "사랑하다"
        )
        for ((surface, base) in cases) {
            assertEquals("규칙 '$surface' → 원형", base, resolve(surface))
        }
    }

    @Test
    fun `불규칙 활용 복원`() {
        val cases = mapOf(
            // ㅂ 불규칙
            "추워" to "춥다", "추웠어" to "춥다", "더워" to "덥다",
            "도와" to "돕다", "도왔어" to "돕다", "구워" to "굽다", "매워" to "맵다",
            "쉬워" to "쉽다", "어려워" to "어렵다", "무거워" to "무겁다",
            "고마워" to "고맙다", "즐거워" to "즐겁다", "반가워" to "반갑다",
            // ㅅ 불규칙
            "나아" to "낫다", "나았어" to "낫다", "지어" to "짓다", "지었어" to "짓다",
            "부어" to "붓다", "저어" to "젓다",
            // 르 불규칙
            "몰라" to "모르다", "몰랐어" to "모르다", "불러" to "부르다",
            "골라" to "고르다", "빨라" to "빠르다", "흘러" to "흐르다",
            // ㅎ 불규칙 (그래는 STOPWORDS라 과거형으로 확인)
            "그랬어" to "그렇다", "빨개" to "빨갛다", "파래" to "파랗다"
        )
        for ((surface, base) in cases) {
            assertEquals("불규칙 '$surface' → 원형", base, resolve(surface))
        }
    }

    // ── 2) 오인식 방지 케이스 (모두 null 이어야 함) ──────────────────
    @Test
    fun `중의성이면 미추천`() {
        // 서로 다른 원형 두 개로 복원될 수 있는 활용형은 추천하지 않는다.
        val ambiguous = listOf(
            "걸어",     // 걷다(walk) / 걸다(hang)
            "걸었어",   // 걷다 / 걸다
            "들었어",   // 듣다(listen) / 들다(lift)
            "물어",     // 묻다(ask) / 물다(bite)
            "물었어"    // 묻다 / 물다
        )
        for (s in ambiguous) assertNull("중의성 '$s' 는 미추천", resolve(s))
    }

    @Test
    fun `접속사 부사는 미추천`() {
        val stopwords = listOf("하지만", "그리고", "그래서", "그래", "그러나", "그런데")
        for (s in stopwords) assertNull("접속·부사 '$s' 는 미추천", resolve(s))
    }

    @Test
    fun `일반명사는 미추천`() {
        // 활용형과 무관한 명사/낱말은 엉뚱한 원형을 만들지 않아야 한다.
        val nouns = listOf(
            "바나나", "어머니", "아버지", "무지개", "김치", "가방", "우리",
            "여기", "저기", "나라", "얼마", "지우개", "손가락"
        )
        for (s in nouns) assertNull("명사 '$s' 는 미추천", resolve(s))
    }

    @Test
    fun `낱자나 영문 섞이면 미추천`() {
        for (s in listOf("ㅁ", "가", "a", "ab", "먹a", "먹 ")) {
            assertNull("'$s' 는 미추천", resolve(s))
        }
    }
}
