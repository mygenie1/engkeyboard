package com.hanyeong.keyboard.hangul

/**
 * 한글 조합에 필요한 '표'들을 모아 둔 곳입니다.
 * 유니코드(컴퓨터가 한글을 저장하는 규칙)에 정해진 순서를 그대로 따릅니다.
 */
object Tables {

    // 초성 19개 (첫 자음이 될 수 있는 자음들)
    const val CHO = "ㄱㄲㄴㄷㄸㄹㅁㅂㅃㅅㅆㅇㅈㅉㅊㅋㅌㅍㅎ"

    // 중성 21개 (모음들. 겹모음 ㅘ,ㅙ 등도 포함)
    const val JUNG = "ㅏㅐㅑㅒㅓㅔㅕㅖㅗㅘㅙㅚㅛㅜㅝㅞㅟㅠㅡㅢㅣ"

    // 종성 28개 (받침. 0번은 '받침 없음' 자리라 실제 글자 대신 빈칸)
    val JONG = listOf(
        "", "ㄱ", "ㄲ", "ㄳ", "ㄴ", "ㄵ", "ㄶ", "ㄷ", "ㄹ", "ㄺ",
        "ㄻ", "ㄼ", "ㄽ", "ㄾ", "ㄿ", "ㅀ", "ㅁ", "ㅂ", "ㅄ", "ㅅ",
        "ㅆ", "ㅇ", "ㅈ", "ㅊ", "ㅋ", "ㅌ", "ㅍ", "ㅎ"
    )

    // 자음 문자 → 초성 번호
    val choOf: Map<Char, Int> = CHO.withIndex().associate { (i, c) -> c to i }

    // 모음 문자 → 중성 번호
    val jungOf: Map<Char, Int> = JUNG.withIndex().associate { (i, c) -> c to i }

    // 홑받침이 될 수 있는 자음 문자 → 종성 번호 (ㄸ,ㅃ,ㅉ은 받침이 될 수 없음)
    val jongOf: Map<Char, Int> = mapOf(
        'ㄱ' to 1, 'ㄲ' to 2, 'ㄴ' to 4, 'ㄷ' to 7, 'ㄹ' to 8,
        'ㅁ' to 16, 'ㅂ' to 17, 'ㅅ' to 19, 'ㅆ' to 20, 'ㅇ' to 21,
        'ㅈ' to 22, 'ㅊ' to 23, 'ㅋ' to 24, 'ㅌ' to 25, 'ㅍ' to 26, 'ㅎ' to 27
    )

    // 겹받침 만들기: (지금 받침 번호, 새로 누른 자음) → 합쳐진 받침 번호
    // 예) (ㄱ=1, ㅅ) → ㄳ=3
    val jongCombine: Map<Pair<Int, Char>, Int> = mapOf(
        (1 to 'ㅅ') to 3,     // ㄱ+ㅅ = ㄳ
        (4 to 'ㅈ') to 5,     // ㄴ+ㅈ = ㄵ
        (4 to 'ㅎ') to 6,     // ㄴ+ㅎ = ㄶ
        (8 to 'ㄱ') to 9,     // ㄹ+ㄱ = ㄺ
        (8 to 'ㅁ') to 10,    // ㄹ+ㅁ = ㄻ
        (8 to 'ㅂ') to 11,    // ㄹ+ㅂ = ㄼ
        (8 to 'ㅅ') to 12,    // ㄹ+ㅅ = ㄽ
        (8 to 'ㅌ') to 13,    // ㄹ+ㅌ = ㄾ
        (8 to 'ㅍ') to 14,    // ㄹ+ㅍ = ㄿ
        (8 to 'ㅎ') to 15,    // ㄹ+ㅎ = ㅀ
        (17 to 'ㅅ') to 18    // ㅂ+ㅅ = ㅄ
    )

    // 겹받침 나누기: 겹받침 번호 → (앞부분 받침 번호, 떨어져 나갈 자음)
    // 도깨비불(연음)과 지우기에서 사용. 예) ㅄ=18 → (ㅂ=17, ㅅ)
    val jongDecompose: Map<Int, Pair<Int, Char>> = mapOf(
        3 to (1 to 'ㅅ'),     // ㄳ → ㄱ + ㅅ
        5 to (4 to 'ㅈ'),     // ㄵ → ㄴ + ㅈ
        6 to (4 to 'ㅎ'),     // ㄶ → ㄴ + ㅎ
        9 to (8 to 'ㄱ'),     // ㄺ → ㄹ + ㄱ
        10 to (8 to 'ㅁ'),    // ㄻ → ㄹ + ㅁ
        11 to (8 to 'ㅂ'),    // ㄼ → ㄹ + ㅂ
        12 to (8 to 'ㅅ'),    // ㄽ → ㄹ + ㅅ
        13 to (8 to 'ㅌ'),    // ㄾ → ㄹ + ㅌ
        14 to (8 to 'ㅍ'),    // ㄿ → ㄹ + ㅍ
        15 to (8 to 'ㅎ'),    // ㅀ → ㄹ + ㅎ
        18 to (17 to 'ㅅ')    // ㅄ → ㅂ + ㅅ
    )

    // 겹모음 만들기: (지금 모음 번호, 새로 누른 모음) → 합쳐진 모음 번호
    // 예) (ㅗ=8, ㅏ) → ㅘ=9
    val jungCombine: Map<Pair<Int, Char>, Int> = mapOf(
        (8 to 'ㅏ') to 9,     // ㅗ+ㅏ = ㅘ
        (8 to 'ㅐ') to 10,    // ㅗ+ㅐ = ㅙ
        (8 to 'ㅣ') to 11,    // ㅗ+ㅣ = ㅚ
        (13 to 'ㅓ') to 14,   // ㅜ+ㅓ = ㅝ
        (13 to 'ㅔ') to 15,   // ㅜ+ㅔ = ㅞ
        (13 to 'ㅣ') to 16,   // ㅜ+ㅣ = ㅟ
        (18 to 'ㅣ') to 19    // ㅡ+ㅣ = ㅢ
    )

    // 겹모음 나누기: 겹모음 번호 → 앞부분 모음 번호 (지우기에서 사용)
    // 예) ㅘ=9 → ㅗ=8
    val jungDecompose: Map<Int, Int> = mapOf(
        9 to 8, 10 to 8, 11 to 8,     // ㅘ,ㅙ,ㅚ → ㅗ
        14 to 13, 15 to 13, 16 to 13, // ㅝ,ㅞ,ㅟ → ㅜ
        19 to 18                      // ㅢ → ㅡ
    )
}
